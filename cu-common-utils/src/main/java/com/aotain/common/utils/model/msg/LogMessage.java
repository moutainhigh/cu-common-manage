package com.aotain.common.utils.model.msg;

import com.aotain.common.utils.model.msg.BaseVo;

/**
 * 日志上报消息
 * 
 * @author silence
 * @time 2018年3月6日
 */
public class LogMessage extends BaseVo {

	private static final long serialVersionUID = 7704804465610583943L;

	private Long statId;
	private Long topTaskId;// 所属任务ID
	private String houseidstr;// 机房编码
	private String smmscommandid;// 管局指令ID
	private Integer data_type;// 数据类型
	private Long statis_time;// 日志上报时间
	private Long record_num;// 日志上报记录数
	private Integer status;// 状态
	private Long createtime;// 创建时间

	public Long getStatId() {
		return statId;
	}

	public void setStatId(Long statId) {
		this.statId = statId;
	}

	public Long getTopTaskId() {
		return topTaskId;
	}

	public void setTopTaskId(Long topTaskId) {
		this.topTaskId = topTaskId;
	}

	public String getHouseidstr() {
		return houseidstr;
	}

	public void setHouseidstr(String houseidstr) {
		this.houseidstr = houseidstr;
	}

	public String getSmmscommandid() {
		return smmscommandid;
	}

	public void setSmmscommandid(String smmscommandid) {
		this.smmscommandid = smmscommandid;
	}

	public Integer getData_type() {
		return data_type;
	}

	public void setData_type(Integer data_type) {
		this.data_type = data_type;
	}

	public Long getStatis_time() {
		return statis_time;
	}

	public void setStatis_time(Long statis_time) {
		this.statis_time = statis_time;
	}

	public Long getRecord_num() {
		return record_num;
	}

	public void setRecord_num(Long record_num) {
		this.record_num = record_num;
	}

	public Integer getStatus() {
		return status;
	}

	public void setStatus(Integer status) {
		this.status = status;
	}

	public Long getCreatetime() {
		return createtime;
	}

	public void setCreatetime(Long createtime) {
		this.createtime = createtime;
	}

}
