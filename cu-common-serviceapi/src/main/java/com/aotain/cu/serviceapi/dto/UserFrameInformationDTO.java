package com.aotain.cu.serviceapi.dto;

import java.io.Serializable;

public class UserFrameInformationDTO extends BaseDTO implements Serializable{
	
	private static final long serialVersionUID = -7139926875538826955L;

	private Long id;
	
	private Long frameId;
	
	private Long houseId; 
	
	private String unitName;
	
	public UserFrameInformationDTO() {
		
	}

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public Long getFrameId() {
		return frameId;
	}

	public void setFrameId(Long frameId) {
		this.frameId = frameId;
	}

	public Long getHouseId() {
		return houseId;
	}

	public void setHouseId(Long houseId) {
		this.houseId = houseId;
	}

	public String getUnitName() {
		return unitName;
	}

	public void setUnitName(String unitName) {
		this.unitName = unitName;
	}
	
	@Override
	public String toString() {
		return "UserFrameInformationDTO [id=" + (id == null ? "null" : id.longValue()) 
				+ ",frameId=" + (frameId == null ? "null" : frameId.longValue()) 
				+ ",houseId=" + (houseId == null ? "null" : houseId.longValue())
				+ ",unitName=" + (unitName == null ? "" : unitName)
				+ "]"
				;
	}
	
}
